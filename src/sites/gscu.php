<h2>Coś na temat uniwersum Grand Strife:</h2>
<p>Jest to fantastyczne uniwersum. Przedstiawia historie bohaterów, którzy wykonują wielkie rzeczy, przy pomocy magii i swojej siły.</p>
<h2>Gry w tym uniwersum:</h2>
<p>Jak do tej pory nie zostały opublikowane żadne gry z tego uniwersum, ale cały czas są prowadzone prace nad jedną z gier. Jest ona typu RPG/MOBA, opiera się na decyzjach gracza na życie różnych osób, jak również na swoje.</p>
